	function get_data($table_name){
		$conn = db_connect();
		$where = "`time_stamp` LIKE '%".date("Y-m-d")."%'";
		$todays_raw_data = select('*',$table_name, $where, $conn);
		foreach ($todays_raw_data as $key => $value) {
			unset($value['id'],$value['time_stamp']);
			foreach ($value as $department_name => $value) {
				$exploded_values = explode("|", $value);
				$todays_data[$key][$department_name] = array_sum($exploded_values);
			}
		}
		return $todays_data;
	}

	function calculate_todays_data($todays_data, $last_key){
		foreach ($todays_data[0] as $key => $value) {
			$final_array[$key] = $value - $todays_data[$last_key][$key];
		}
		return $final_array;
	}

	function calculate_total_data($todays_data, $last_key, $default_data){
		foreach ($todays_data[$last_key] as $key => $value) {
			$final_array[$key] = $default_data[$key] - $value;
		}
		return $final_array;
	}

	function get_selected_college_data($college_name, $default_aproved_intake){
		$todays_raw_data = get_data('`'.$college_name.'`');
		$last_array = count($todays_raw_data) - 1;
		$todays_data = calculate_todays_data($todays_raw_data,$last_array);
		$total_data = calculate_total_data($todays_raw_data, $last_array, $default_aproved_intake[$college_name]);
		foreach ($total_data as $key => $value) {
			$final_array[$college_name][$key]['total'] = $default_aproved_intake[$college_name][$key];
			$final_array[$college_name][$key]['today'] = $todays_data[$key];
			$final_array[$college_name][$key]['overall'] = $total_data[$key];
		}
		$final_array[$college_name]['grand_total']['total'] = 0;
		$final_array[$college_name]['grand_total']['today'] = 0;
		$final_array[$college_name]['grand_total']['overall'] = 0;
		foreach ($final_array[$college_name] as $key => $value) {
			$final_array[$college_name]['grand_total']['total'] += $value['total'];
			$final_array[$college_name]['grand_total']['today'] += $value['today'];
			$final_array[$college_name]['grand_total']['overall'] += $value['overall'];
		}
		return $final_array;
	}
